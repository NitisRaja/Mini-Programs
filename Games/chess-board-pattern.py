for row in range(1,8+1):
    shaded = (" |" * 5)
    unshaded = (" " * 9) + "|"
    shaded_with_top = ("‾|" * 5) # ‾ is overline char
    shaded_with_bottom = ("_|" * 5)

    # first line with top border
    print("|",end="")
    for i in range(4):
        if row==1:
            print(shaded_with_top + ("‾"*9 + "|"), end="")
        elif row%2==1:
            print(shaded_with_top + unshaded, end="")
        else:
            print(unshaded + shaded_with_top, end="")
    print()

    # middle 2 lines
    for i in range(2):
        print("|",end="") # start of single line
        for j in range(4):
            if row%2==1:
                print(shaded + unshaded, end="")
            else:
                print(unshaded + shaded, end="")
        print() # end of single line

    # last line with bottom border
    print("|",end="")
    for i in range(4):
        if row==8:
            print(("_"*9 + "|") + shaded_with_bottom, end="")
        elif row%2==1:
            print(shaded_with_bottom + unshaded, end="")
        else:
            print(unshaded + shaded_with_bottom, end="")
    print()

